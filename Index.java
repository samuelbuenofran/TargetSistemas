package eg;

public class Index {
    public static void main(String[] args) {

        int index = 13;
        int soma = 0;
        int k = 0;

        while (k < index) {

            k = k + 1;

            soma = soma + k;

        }

        System.out.println(soma);

        // Imprime 91
        // It outputs 91

    }
}
